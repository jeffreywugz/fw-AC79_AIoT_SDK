unsigned char wrGC2145Reg(unsigned char regID, unsigned char regDat)
{
    u8 ret = 1;
    dev_ioctl(iic, IIC_IOCTL_START, 0);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, GC2145_DVP_WRCMD)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);
    if (dev_ioctl(iic, IIC_IOCTL_TX, regID)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_STOP_BIT, regDat)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);

exit:
    dev_ioctl(iic, IIC_IOCTL_STOP, 0);
    return ret;
}

unsigned char rdGC2145Reg(unsigned char regID, unsigned char *regDat)
{
    u8 ret = 1;
    dev_ioctl(iic, IIC_IOCTL_START, 0);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, GC2145_DVP_WRCMD)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_STOP_BIT, regID)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, GC2145_DVP_RDCMD)) {
        ret = 0;
        printf("iic write err!!! line : %d \n", __LINE__);
        goto exit;
    }
    delay(DELAY_TIME);
    dev_ioctl(iic, IIC_IOCTL_RX_WITH_STOP_BIT, (u32)regDat);

exit:
    dev_ioctl(iic, IIC_IOCTL_STOP, 0);
    return ret;

}

void GC2145_config_SENSOR(u16 *width, u16 *height, u8 *format, u8 *frame_freq)
{
    int i;
    for (i = 0; i < sizeof(GC2145_INI_REG) / sizeof(Sensor_reg_ini); i++) {
        wrGC2145Reg(GC2145_INI_REG[i].addr, GC2145_INI_REG[i].value);
    }
    *format  = SEN_IN_FORMAT_YUYV;
    puts("\nGC2145 YUYV\n");
}
s32 GC2145_set_output_size(u16 *width, u16 *height, u8 *freq)
{
    u16 w = *width + 32;//宽多加32像素点
    u16 h = *height + 8;//高多加8像素点
    wrGC2145Reg(0xfe, 0x00);
    wrGC2145Reg(0x0f, (w >> 8) & 0xff);
    wrGC2145Reg(0x10, w & 0xff);
    wrGC2145Reg(0x0d, (h >> 8) & 0xff);
    wrGC2145Reg(0x0e, h & 0xff);
    return 0;
}

s32 GC2145_ID_check(void)
{
    u8 pid = 0x00;
    u8 ver = 0x00;
    u8 i ;

    for (i = 0; i < 3; i++) { //
        rdGC2145Reg(0xf0, &pid);
        rdGC2145Reg(0xf1, &ver);
    }

    puts("Sensor PID \n");
    puts("\n");

    if (pid != 0x21 || ver != 0x45) {
        puts("\n----not GC2145_DVP-----\n");
        return -1;
    }
    puts("\n----hello GC2145_DVP-----\n");
    return 0;

}
static s32 GC2145_powerio_ctl(u8 _power_gpio, u8 on_off)
{
    gpio_direction_output(_power_gpio, on_off);
    return 0;
}
void GC2145_reset(u8 isp_dev)
{
    u8 res_io;
    u8 powd_io;
    u8 id = 0;
    puts("GC2145 reset\n");

    if (isp_dev == ISP_DEV_0) {
        res_io = gc2145_reset_io[0];
        powd_io = gc2145_power_io[0];
    } else {
        res_io = gc2145_reset_io[1];
        powd_io = gc2145_power_io[1];
    }

    if (powd_io != (u8) - 1) {
        GC2145_powerio_ctl(powd_io, 0);
    }
    if (res_io != (u8) - 1) {
        gpio_direction_output(res_io, 1);
        gpio_direction_output(res_io, 0);
        os_time_dly(1);
        gpio_direction_output(res_io, 1);
    }

}

s32 GC2145_check(u8 isp_dev, u32 _reset_gpio, u32 _power_gpio)
{
    if (!iic) {
        if (isp_dev == ISP_DEV_0) {
            iic = dev_open("iic0", 0);
        } else {
            iic = dev_open("iic1", 0);
        }
        gc2145_reset_io[isp_dev] = (u8)_reset_gpio;
        gc2145_power_io[isp_dev] = (u8)_power_gpio;
    }
    if (iic == NULL) {
        printf("GC2145 iic open err!!!\n\n");
        return -1;
    }
    GC2145_reset(isp_dev);
    if (0 != GC2145_ID_check()) {
        dev_close(iic);
        iic = NULL;
        return -1;
    }

    return 0;
}

u16 GC2145_dvp_rd_reg(u16 addr)
{
    u8 val;
    rdGC2145Reg((u8)addr, &val);
    return val;
}

void GC2145_dvp_wr_reg(u16 addr, u16 val)
{
    wrGC2145Reg((u8)addr, (u8)val);
}

void set_rev_sensor_GC2145(u16 rev_flag)
{
    if (!rev_flag) {
        wrGC2145Reg(0x17, 0x12);
    } else {
        wrGC2145Reg(0x17, 0x11);
    }
}
static s32 GC2145_init(u8 isp_dev, u16 *width, u16 *height, u8 *format, u8 *frame_freq)
{
    puts("\n\n GC2145_init \n\n");

    if (0 != GC2145_check(isp_dev, 0, 0)) {
        return -1;
    }

    GC2145_config_SENSOR(width, height, format, frame_freq);

    return 0;
}

