int http_cb(void *httpcli_ctx, void *buf, unsigned int size, void *priv, httpin_status status)
{
    if (status == HTTPIN_HEADER) {
        printf("header message: %s\n", buf);
    }

    if (buf) {
        printf("received data : %s\n");
    }

    return 0;
}

static void http_test_task(void *priv)
{
    int ret = 0;
    http_body_obj http_body_buf;
    char *user_header_buf = NULL;
    httpcli_ctx ctx = {0};
    int http_more_data[3] = {0};
    int http_more_data_len[4] = {0};

    user_header_buf = calloc(1, USER_HEADER_BUF_SIZE);
    if (!user_header_buf) {
        goto exit;
    }

    http_body_buf.recv_len = 0;
    http_body_buf.buf_len = 4 * 1024;
    http_body_buf.buf_count = 1;
    http_body_buf.p = (char *) malloc(http_body_buf.buf_len * http_body_buf.buf_count);
    if (!http_body_buf.p) {
        goto exit;
    }

    sprintf(http_body_begin, BODY_OPTION_ONE);
    sprintf(http_body_end, BODY_OPTION_END);

    http_more_data[0] = (int)http_body_begin;
    http_more_data[1] = (int)music_data;                      //音乐数据
    http_more_data[2] = (int)http_body_end;
    http_more_data_len[0] = strlen(http_body_begin);
    http_more_data_len[1] = sizeof(music_data);
    http_more_data_len[2] = strlen(http_body_end);
    http_more_data_len[3] = 0;

    ctx.more_data = http_more_data;
    ctx.more_data_len = http_more_data_len;

    sprintf(user_header_buf, HTTP_HEAD_OPTION,  strlen(http_body_begin) + sizeof(music_data) + strlen(http_body_end));

    ctx.url = HTTP_DEMO_URL;
    ctx.cb = http_cb;
    ctx.timeout_millsec = 5000;
    ctx.priv = &http_body_buf;
    ctx.connection = "close";

    //自定义头部
    ctx.user_http_header = user_header_buf;

    ret = httpcli_post(&ctx);
    if (ret != HERROR_OK) {
        printf("http client test faile\n");
    } else {
        if (http_body_buf.recv_len > 0) {
            printf("\nReceive %d Bytes from (%s)\n", http_body_buf.recv_len, HTTP_DEMO_URL);
            printf("%s\n", http_body_buf.p);
        }
    }

exit:
    //关闭连接
    httpcli_close(&ctx);

    if (http_body_buf.p) {
        free(http_body_buf.p);
        http_body_buf.p = NULL;
    }

    if (user_header_buf) {
        free(user_header_buf);
        user_header_buf = NULL;
    }
}

static void http_user_header_test(void *priv)
{
    enum wifi_sta_connect_state state;
    while (1) {
        printf("Connecting to the network...\n");
        state = wifi_get_sta_connect_state();
        if (WIFI_STA_NETWORK_STACK_DHCP_SUCC == state) {
            printf("Network connection is successful!\n");
            break;
        }
        os_time_dly(100);
    }

    if (thread_fork("http_test_task", 10, 4 * 1024, 0, NULL, http_test_task, NULL) != OS_NO_ERR) {
        printf("thread fork fail\n");
    }
}

